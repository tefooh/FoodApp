// Helper to normalize Arabic text for comparison
const normalizeArabic = (text) => {
    if (!text) return '';
    return text
        .trim()
        .replace(/[\u064B-\u065F]/g, '') // Strip diacritics
        .replace(/[أإآ]/g, 'ا') // Normalize Alefs
        .replace(/ة/g, 'ه')     // Normalize Ta Marbuta
        .replace(/ى/g, 'ي');    // Normalize Ya
};

export const translateText = (text, lang) => {
    if (!text) return '';
    const cleanText = text.trim();

    if (lang === 'en') {
        // Source is typically Arabic (from DB) -> Target English

        // 1. Direct Lookup
        if (ARABIC_TO_ENGLISH[cleanText]) return ARABIC_TO_ENGLISH[cleanText];

        // 2. Normalized Lookup
        const normalizedInput = normalizeArabic(cleanText);
        const arabicMatch = Object.keys(ARABIC_TO_ENGLISH).find(key => normalizeArabic(key) === normalizedInput);
        if (arabicMatch) return ARABIC_TO_ENGLISH[arabicMatch];

        // 3. Check if already English (Look in Values of Arabic Map or Keys of English Map)
        if (ENGLISH_TO_ARABIC[cleanText]) return cleanText; // It's a known English Key
        const isEnglishValue = Object.values(ARABIC_TO_ENGLISH).includes(cleanText);
        if (isEnglishValue) return cleanText;

        return cleanText;
    } else {
        // Target Arabic

        // 1. Direct Lookup (English Key -> Arabic Value)
        if (ENGLISH_TO_ARABIC[cleanText]) return ENGLISH_TO_ARABIC[cleanText];

        // 2. Case Insensitive Lookup
        const lowerInput = cleanText.toLowerCase();
        const englishMatch = Object.keys(ENGLISH_TO_ARABIC).find(key => key.toLowerCase() === lowerInput);
        if (englishMatch) return ENGLISH_TO_ARABIC[englishMatch];

        // 3. Check if already Arabic
        if (ARABIC_TO_ENGLISH[cleanText]) return cleanText; // It's a known Arabic Key
        if (normalizeArabic(cleanText) === cleanText || /[\u0600-\u06FF]/.test(cleanText)) return cleanText; // Looks like Arabic

        return cleanText;
    }
};
